package up

import "testing"

func TestGrouping(t *testing.T) {
	a := []string{"", "1", "12", "123", "1234", "12345", "123456", "1234567", "12345678", "123456789", "1234567890", "-", "-1", "-12", "-123", "-1234", "-12345", "-123456", "-1234567", "-12345678", "-123456789", "-1234567890"}
	b := []string{"", "1", "12", "123", "1_234", "12_345", "123_456", "1_234_567", "12_345_678", "123_456_789", "1_234_567_890", "-", "-1", "-12", "-123", "-1_234", "-12_345", "-123_456", "-1_234_567", "-12_345_678", "-123_456_789", "-1_234_567_890"}
	for i, s := range a {
		r := Grouping(s)
		if r != b[i] {
			t.Error(r, "should be:", b[i], "for:", s)
		}
	}
}

func TestAbs(t *testing.T) {
	if Abs(-2) != 2 || Abs(2) != 2 {
		t.Error("Abs")
	}
}

func TestMin(t *testing.T) {
	if Min(5, 2) != 2 || Min(2, 5) != 2 {
		t.Error("Min")
	}
}

func TestMax(t *testing.T) {
	if Max(5, 2) != 5 || Max(2, 5) != 5 {
		t.Error("Max")
	}
}
